# method signatures

# add method
def add(a: int, b: int) -> int:
    # return sum of passed integers
    # print("Adding", a, "and", b)
    return a + b

# subtract method
def subtract(a: int, b: int) -> int:
    # print("Subtracting", a, "and", b)
    # return difference between a and b
    return a - b


print(add(3, 5))